    private void btnAddWishlistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddWishlistActionPerformed
                                           
    try {
        Connection con = ConnectionFactory.getConnection();
        String sql = "INSERT INTO wishlist (nome, preco, descricao, plataforma) VALUES (?, ?, ?, ?)";
        PreparedStatement stmt = con.prepareStatement(sql);

        stmt.setString(1, lblNomeJogo.getText());
        stmt.setString(2, lblPreco.getText());
        stmt.setString(3, descricaoOriginal);
        stmt.setString(4, lblPlataforma.getText());

        stmt.executeUpdate();
        JOptionPane.showMessageDialog(this, "Jogo adicionado Ã  wishlist!");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao adicionar: " + e.getMessage());
    }

    }//GEN-LAST:event_btnAddWishlistActionPerformed

